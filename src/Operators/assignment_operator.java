package Operators;

public class assignment_operator {
	
	public static void main(String[] args) {
		
		int i=10;
		
		System.out.println(i+=10);
		
		System.out.println(i-=10);
		
		System.out.println(5/2f);//   (/is the division)
		
		System.out.println(5%2);//   % it always give reminder in result
		
		
		
		
	}

}
