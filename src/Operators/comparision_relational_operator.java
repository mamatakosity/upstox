package Operators;

public class comparision_relational_operator {
	
	public static void main(String[] args) {
		
		System.out.println(2==3);//result is false
		
		System.out.println(3>2);//result is true
		
		System.out.println(3>=2);//result is true
		
		System.out.println(3<2);//result is false
		
		System.out.println(3<=2);//result is false
		
		System.out.println(3!=2);//result is true
	}

}
