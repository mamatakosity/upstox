public class Demo_10 {

	
		
		//Addition with parameter
		
		public static void main(String[] args) {//main method open
			
			addition(90,65,89);
			
			
			
		}//main method closed
		
		
		public static void addition(int a,int b,int c) {
			
		
			
			int d= a+b+c;
			
			System.out.println("Addition d = "+d);
			
		}
		
		

	}


