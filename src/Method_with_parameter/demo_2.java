package Method_with_parameter;

public class demo_2 {
	public static void main(String[]args) {
		multi(12,12);
		add(256,256);
		
	}
	public static void multi(int a,int b) {
		int c=a*b;
		System.out.println(c);
	}
		public static void add(int s,int y) {
			
			int z=s+y;
			
			System.out.println(z);
		}
		
	

}
