package Method_overloading;

public class addition {
	
public void add(int a,int b) {//method name add
		
		System.out.println(a+b);
		
	}
	
	public void add(int a,int b,int c) {//method name add//both mathed name are same but their arguments are diff
		
		System.out.println(a+b+c);
	}

}
