package Conditional_statement_If_else_ladder;

public class if_else_ladder_pr9 {
	
		public static void main(String[] args) {
			
			int marks=20;
			
			if(marks>=95) {
				System.out.println("Student is fail");
			
			}
			
			else if(marks>=75) {
				
				System.out.println("Student is pass in 3 class");
			}
			
			else if(marks>=60) {
				
				System.out.println("Student is pass in 2 class");
			}
			
		
			else if(marks>=45) {
				
				System.out.println("Student is pass in 1 class");
			}
			
			else {
				
				System.out.println("Student is pass distinction class");
			}
			
		}

	}



