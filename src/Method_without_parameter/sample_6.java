package Method_without_parameter;

public class sample_6 {
	
	

		
		//Addition without parameter
		
		public static void main(String[] args) {//main method open
			
			addition();
			
			
			
		}//main method closed
		
		
		public static void addition() {
			
			int a=10;
			int b=20;
			int c=30;
			
			int d= a+b+c;
			
			System.out.println("Addition d = "+d);
			
		}
		
		

	}

