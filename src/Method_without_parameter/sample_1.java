package Method_without_parameter;

public class sample_1 {//class body open
	
	public static void main(String[]args) {// main method body open
		
		addition();//method calling statement
		
	}//main method body closed
	
	public static void addition() {//called method
		int a=10;            //() nothing is written inside the bracket so this is without parameter method
		int b=20;                   
		int c=a+b;
		System.out.println(c);
		
		
	}

}//class body closed
