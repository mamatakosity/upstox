package Nested_for_loop;

public class star_pattern_4X4_matrix {
	
	public static void main(String[] args) {
		
		for(int i=1;i<=4;i++) {//condition for row
			
			for(int j=1;j<=4;j++) {//condition for column
				
				System.out.print("*");
			}
			
			System.out.println("*");
			
		}//inner for loop closed
		
		
	}

}
